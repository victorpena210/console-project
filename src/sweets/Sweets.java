package sweets;

public abstract class Sweets {
	private String name;
	private double weight;
	


	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}

	
	
	
	
	
	

}
